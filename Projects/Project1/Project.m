function [x_r] = NewtonRaphson(f,df,x_i,e_s,mi)
    %NewtonRaphson:this function is used to estimate the roots of the given equation
    %Inputs:
    %   f = the function
    %   df = its derivative
    %   x_i = intial guess
    %   e_s = tolerance
    %   mi = maximum number of iterations
    %Outputs
    %   a = the aproximation of the root
    if nargin<3,error('at least 3 input arguments required'),end
    if nargin<4|isempty(e_s),e_s=0.0001;end
    if nargin<5|isempty(mi),mi=50;end
    x_r = x_i;
    it = 0;
    while true
       old = x_r;
       if df(x_i) == 0
        error("Newton Raphson can not be used when the derivative is zero")
       end
       x_r = x_r - f(x_r)/df(x_r);
       it = it +1;
       if x_r ~= 0
           e_a = abs((x_r-old)/(x_r))*100; 
       end
       if it > mi || e_a < e_s
           break
       end
    end

 function [R] = Range(lambda,alpha,v_0,R_i,e_s,g)
        %Range: this function approximate the range of projectile in resistance
        %medium
        %Inputs:
        %   lambda the proportional constant
        %   alpha: angle
        %   v_0: intial velocity
        %   e_s: tol
        %Outputs:
        %   Range
        f = @(R) ((v_0*sin(alpha)+g/lambda)/v_0*cos(alpha))*R + g/lambda^2 * log(1-(lambda*R/(v_0*cos(alpha))));
        df = @(R) ((v_0*sin(alpha)+g/lambda)/(v_0*cos(alpha))) - g/(lambda*(v_0*cos(alpha)-lambda*R_i));
        R = NewtonRaphson(f,df,R_i,e_s,1000);

 function [T] = Time(lambda,alpha,v_0,T_i,e_s,g)
    f = @(T) (v_0*sin(alpha)+g/lambda)/lambda *(1-exp(-lambda*T)) - g/lambda *T;
    df = @(T) (v_0*sin(alpha)+g/lambda)*exp(-lambda*T_i) - g/lambda;
    
    T = NewtonRaphson(f,df,T_i,e_s,1000);
   

